            <p>
                     Le projet est donc de proposer une application web de décision collective permettant
                de gérer un grand ensemble de questions et de réponses.
                Le principe est qu’un utilisateur soumet une idée de vote à l’administrateur. Cette idée, si elle est validée,
                donne lieu à la création d’un vote par cet utilisateur qui devient donc organisateur.
            </p>
            <p>      L’organisateur précise l’objet de sa consultation, propsoe un plan, précise un calendrier (début et fin d’écriture des réponses, début et fin des votes), choisit les votants, et choisit les reponsables de
                texte qui pourront proposer une réponse parmi une liste d’utilisateurs.</p>
            <p>
                     Les responsables de texte peuvent choisir des co-auteurs parmi une liste d’utilisateurs, qui pourront l’assister à l’écriture des réponses. Les responsables choisissent quelles section les co-auteurs peuvent modifier.
            </p>
            <p>
                     Enfin, un fois l’écriture des réponses terminée, les votants inscrits au vote pourront voter pour leur proposition préféree, et, au à la fin de la période de vote, la proposition avec le plus de votes gagne, et est rendue publique.
            </p>
